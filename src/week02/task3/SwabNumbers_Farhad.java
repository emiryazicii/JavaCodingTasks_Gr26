package week02.task3;

public class SwabNumbers_Farhad {
    public static void main(String[] args) {
        
        
        int a = 10;
        int b = 5;

        System.out.println("a = " + a);
        System.out.println("b = " + b);
        System.out.println("-------------------------");
        
        a = a + b; //10+5=15
        b = a - b; //15-5=10
        a = a - b; //15-10=5

        System.out.println("a = " + a);
        System.out.println("b = " + b);
    }
    
    
    
    
    
    /*
    Swap two variable values without using a third variable
     */
}
